                        <p>
                            Margaret Bourke-White fut d’abord spécialisée dans la photographie industrielle, ayant ouvert un studio en 1927 à Cleveland. Ainsi, cette dernière a travaillé pour diverses entreprises industrielles, et notamment pour la compagnie Otis, spécialisée dans la sidérurgie. D’après son autobiographie Portrait de moi-même, la jeune Margaret rencontre alors différentes difficultés techniques au moment de la prise de vue dans l’usine : en effet, l’acier en fusion produit des lumières rouges et oranges, ce qui empêche le film de son appareil, sensible à la lumière bleue, de capter une source lumineuse suffisante pour avoir un rendu final quelque peu visible. Pour résoudre ce problème de lumière, elle a besoin d’une source lumineuse suffisamment puissante pour capter ces scènes, elle utilise alors un éclairage au magnésium qui permet de diffuser une lumière blanche. On peut faire l’hypothèse qu’elle a utilisé ce qu’on appelle une “ampoule flash”, inventée par l’allemand Paul Vierkoetter en 1925. On remarque d’ailleurs dans cette photographie de son matériel issu du magazine Life un objet qui pourrait correspondre à cette fameuse ampoule.
