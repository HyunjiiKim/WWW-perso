                        <p>
                            Enfin, en tant que femme photojournaliste, Margaret Bourke-White accorde un rôle majeur aux femmes dans ses photographies : en effet, un bon nombre de ses clichés nous présentent ces dernières comme les actrices principales de leur vie et de leur société, et ce notamment durant la Seconde Guerre mondiale (1939-1945). Durant cette période,  afin de remplacer les hommes partis au combat, les femmes ont joué un rôle majeur dans différents aspects de la guerre, notamment dans les domaines de l’agriculture ou de l’industrie, en utilisant parfois des machines spectaculaires : d’après un numéro du magazine Life sorti le 9 août 1943, “parmis les 16 000 000 femmes employés aux Etats-Unis, plus du quart travaillent dans l’industrie de la guerre”. Ces femmes sont ainsi mises au centre de nombreuses photographies de la photojournaliste américaine, comme par exemple dans ce cliché datant de 1943 où l’on voit des femmes biseauter des plaques de blindage pour un tank américain à l’aide de torches d’acétylènes…<br>
                            Ce rôle des femmes dans l’industrie de la guerre aux Etats-Unis est le sujet d’une collection de photographies prises par Margaret Bourke-White pour le magazine Life, que vous pouvez retrouver dans <a href="https://www.life.com/history/women-of-steel-life-with-female-factory-workers-in-world-war-ii/">cet article</a>.                        </p>
