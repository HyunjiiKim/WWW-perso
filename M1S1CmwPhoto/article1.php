                            <p>
                                Le talent de Margaret Bourke-White en photographie s’est grandement manifesté à travers ses clichés les plus percutants humainement : en effet, cette dernière a capturé divers épisodes historiques dans lesquels les femmes et les hommes sont de réels acteurs sociaux. 
                            <br>
                            Les photographies de Margaret Bourke-White peuvent ainsi s'inscrire dans le mouvement du réalisme social, également appelé photographie sociale : ce mouvement se concentre sur la représentation de la société à travers ses différents aspects et problématiques, telles que la pauvreté, la discrimination, la guerre, etc. Le réalisme social est également une arme pour les artistes, dont les photographes, pour critiquer l’ordre politique et social d’un pays. Il est important de distinguer le <strong>réalisme social</strong> du <strong>réalisme socialiste</strong>, mouvement issu de la doctrine stalinienne du milieu du XXème siècle dont le but était la propagande de la vie en Union soviétique à travers des œuvres d’art. 
                            <br>
                            Le réalisme social prend de l’importance aux Etats-Unis durant la Grande Dépression (1929-1939), et c’est durant cette période que la photojournaliste américaine prend des clichés symboliques de la vie sur le continent nord-américain, dans un contexte de crise. Margaret Bourke-White se rend notamment dans le sud des Etats-Unis en 1936 aux côtés de son époux, l'auteur Erskine Caldwell, pour documenter les conditions d’extrême pauvreté ainsi que la discrimination de la communauté afro-américaine. Cette expérience lui permet de développer un aspect plus social dans ses clichés, en s’intéressant davantage aux humains dans leur environnement plutôt qu’aux machines et bâtiments comme elle le faisait dans ses photographies industrielles. 

                            </p></div>
                    </div>
                    <div class="imgwth">
                        <p>
                            Pendant 18 mois, Margaret Bourke-White prend en photo différentes scènes dans lesquelles transparaissent les difficultés que subissent les populations dans les espaces ruraux les plus pauvres de cette région, en attendant parfois pendant plusieurs heures pour capturer le moment parfait. Le couple publie ces clichés dans un ouvrage intitulé You have seen their faces (“vous avez vu leurs visages”) en 1937.
                            <br>
                            <br>
