                            <br>
                            Il est impossible d’évoquer le remarquable travail de Bourke-White sans parler de son matériel : dans ce court article, nous présenterons les différents appareils que la photographe américaine a pu utiliser au cours de sa carrière et qui lui ont permis de nous offrir des clichés iconiques. 
                            <br>
                            Il faut savoir qu’afin de suivre ses premiers cours de photographie auprès du photographe Clarence H.White, Bourke-White reçoit, comme cadeau de la part de sa mère, son premier appareil en 1921: il s’agissait d’un Ica Reflex 3 ¼ x 4 ¼ , avec un un objectif fissuré. Ses premières photographies ont été prises sur des plaques de verre. Cet appareil, commercialisé en 1909, était doté d’un mono objectif, avec une mise au point possible grâce à une platine. 
