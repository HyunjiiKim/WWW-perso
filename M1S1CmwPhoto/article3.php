                        <p>Il faut tout d’abord savoir que le photojournalisme est à son pic dans les années 1930 : en effet, de plus en plus de photoreporter vont voyager à travers le monde pour couvrir les différents conflits historiques qui éclatent durant cette période, et particulièrement la Seconde Guerre mondiale : de grands photojournalistes partagent ainsi au monde des clichés iconiques, tels que Robert Capa, Gerda Taro, ou encore David Seymour. C’est parmi ces célèbres noms que Bourke-White va construire une carrière brillante et capturer des scènes qui marqueront l’histoire : l’exemple parfait est cette photographie du camp de Buchenwald en 1945 après sa libération par les troupes américaines.</p>
