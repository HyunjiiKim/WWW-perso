                    <div class="text">
                        <p>D’après Sylvie Zaidman, historienne et directrice du musée de la Libération de Paris, dans lequel a été tenue une exposition intitulée <a href=”https://www.museeliberation-leclerc-moulin.paris.fr/exhibitions/femmes-photographes-de-guerre>”Femmes photographes de guerre</a>, le point de vue des femmes sur la guerre n’est pas en soi différent. En effet, il s’agit d’un contexte tragique, et peu importe le sexe, la manière de documenter une guerre ne diffère pas. Cependant, cette dernière explique dans <a href=”https://www.radiofrance.fr/franceculture/podcasts/affaire-en-cours/les-femmes-des-photographes-de-guerre-comme-les-autres-3025342”>une interview pour RadioFrance</a> que les femmes ont souvent plus de facilité à entrer dans l’intimité des victimes et ainsi proposer un aspect plus humain dans leurs photographies. Dans le cas de Margaret Bourke-White, la photojournaliste a réussi à collecter de nombreux clichés de guerre emblématiques et mémorables, qui ont marqué les esprits de par les personnages qui y sont représentés et surtout l’exclusivité de ces photographies. En effet, cette dernière est reconnue pour son audacité et son courage, qualités qui lui ont permis de documenter des événements en avant-première : elle a été le premier photographe d’occident à se rendre en Russie pour documenter la propagande en URSS en 1930. Elle partage ainsi au monde entier des clichés exclusifs des installations industrielles en Russie, des portraits de femmes et d’hommes, mais également des scènes de vie. </p>
