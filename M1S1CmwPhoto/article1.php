                            <p>
                                Ce cliché seul illustre donc le réalisme social chez Margaret Bourke-White : cette photographie permet aux spectateurs de réfléchir sur les conditions des populations afro-américaines et les discriminations qu’elles subissent, mais également la paupérisation de ces zones rurales et les injustices auxquelles elles sont soumises, montrant ainsi l’écart considérable entre cette fameuse “american way of life” et la réalité difficile des populations défavorisées aux États-Unis durant la Grande Dépression.
                            </p>
