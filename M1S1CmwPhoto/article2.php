                        <p>Les deux photographies nous permettent d’avoir un aperçu du travail de Bourke-White en Russie : l’une représente des bébés bolcheviks dans une école maternelle, et l’autre de la danseuse soviétique Marina Semyonova. Ces deux clichés sont exceptionnels pour l’époque, l’occident n’ayant jusqu’ici qu’une vision très politique de l’URSS. Ici, Margaret Bourke-White nous montre des scènes issues du quotidien de ces peuples soviétiques, à travers la vie de différents types de travailleurs, de classes sociales, de différentes tranches d’âge, etc. 
                            <br>
                            L’intimité dans ces photographies mettant en scène la vie collective et individuelle en URSS  nous rappelle ainsi les propos de l’historienne Sylvie Zaidmann : les femmes photographes ont-elle plus de facilité que leurs confrères pour s'immerger dans l’intimité des individus, et était-ce le cas pour Bourke-White? On peut en tout cas en faire l’hypothèse grâce aux nombreux clichés de cette dernière qui nous offre un point de vue unique sur le quotidien en URSS durant les années 30. 
                            <br>
                            En tant que pionnière dans son domaine, Margaret Bourke-White est donc l’exemple parfait pour illustrer l’ascension des femmes dans le monde du photojournalisme et de la photographie en général : son courage et ses grandes ambitions lui ont permis de se créer une place parmi ses collègues masculins, voire même de les surpasser dans de nombreuses situations. 
                            </p>
