                    <p>Le domaine de la photographie de guerre étant majoritairement masculin, il est important de constater que malgré tout, cela n’a pas empêché de nombreuses femmes de se forger une place dans cet univers, telles que Gerda Taro, Lee Miller, Susan Meiselas, et bien sûr, Margaret Bourke-White, qui fut une pionnière parmi ces femmes photographes de guerre. 
                        Mais une question se pose : leur point de vue sur la guerre est-il différent de leurs collègues masculins? Nous tenterons d’y répondre à travers cet article et toujours à l’appui de l'histoire de la photographe américaine. 
