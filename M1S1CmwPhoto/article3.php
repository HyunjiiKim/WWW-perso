                        <p>“Je dois travailler avec un voile qui enveloppe mon esprit. En photographiant ces camps d’extermination, ce voile était si fermement serré que j’avais du mal à savoir ce que j’avais capturé jusqu’au moment où j’ai vu les tirages des mes propres photos [...].” Cette citation, issue d’une biographie de Margaret Bourke-White écrit par Vicki Goldberg en 1988, décrit parfaitement la dure réalité du métier de photojournaliste.<br> 
                            Documenter la guerre est un exercice des plus éprouvants : le photojournaliste doit retransmettre des scènes sanglantes et traumatisantes au monde entier, afin de ne pas laisser inaperçu ces massacres et immortaliser la mémoire des victimes. La carrière de Margaret Bourke-White nous permet d’avoir un aperçu du travail d’un photojournaliste en tant de guerre, et pas des moindre : cette dernière a été la première femme photojournaliste à se rendre sur les fronts de bataille durant la Seconde Guerre mondiale, ainsi que la seule photographe venue d’occident à témoigner de l’invasion de Moscou par l’Allemagne en 1941. Nous allons ainsi parcourir quelques épisodes importants de la carrière de photojournaliste de Margaret Bourke-White pour tenter de comprendre son approche sur le photojournalisme de guerre.
