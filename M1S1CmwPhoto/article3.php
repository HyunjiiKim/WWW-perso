                        <p>
                            La photojournaliste a ainsi pu témoigner de l’horreur de l’un des plus grands camp de concentration Nazi et partager le portrait des survivants de ce massacre : ce cliché capture les différentes émotions ressenties par ces hommes lors de leur libération, après le cauchemar qu’ils ont vécu au sein de de ce camp. Bourke-White a donc dû faire face, aux côtés de l’armée américaine, à la réalité macabre des actions du IIIème Reich. Elle partage son témoignage en 1946 dans un ouvrage intitulé “Dear Fatherland, Rest Quietly”, dans lequel elle mentionne le jour de la libération du camp de Buchenwald et sa réaction face à la vue des victimes de ce crime : “l’usage de mon appareil était un soulagement. Cela a permis de créer une légère barrière entre moi et l’horreur blanche qui me faisait face” (“l’horreur blanche” fait référence à la neige qui tombait ce jour-là). Cette expérience a profondément marquée et choquée Margaret Bourke-White, et elle partage ainsi ces scènes d’horreur à travers ses photographies d’hommes et de femmes qui ont vécu l’enfer, comme en témoigne cette autre photographie, qui est sans doute l’un des clichés les plus marquants de sa carrière :
